fruta = input("Digite sua fruta: ").upper()
if  (fruta == "BANANA"):
    print("O preço do kilo da banana é 5,23 reais")
elif (fruta == "maçã"):
    print("O preço do kilo da maçã é 12,10 reais")
elif (fruta == "cereja"):
    print("O preço do kilo da cereja é 58,00 reais")

else:
    print("Desculpe, a fruta", fruta, "está em falta!")